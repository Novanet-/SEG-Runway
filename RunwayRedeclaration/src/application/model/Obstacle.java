package application.model;

public class Obstacle
{

	private String	name;
	private double	height, width, length;

	public String getName()
	{
		return name;
	}

	public double getHeight()
	{
		return height;
	}

	public double getWidth()
	{
		return width;
	}

	public double getLength()
	{
		return length;
	}

}